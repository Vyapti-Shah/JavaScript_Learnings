//Template Literal - delimited with (``)
//                 - instead of double or single quotes allows  embedded variables and expressions

let name = "Vyapti";
let age = 19;
let isSmart = true;

console.log("Name is", name , "Shah");
console.log("Age is", age, "years");
console.log("She is a smart girl", isSmart);

//using Template Literals (``)
console.log(`Name is ${name} Shah`);
console.log(`Age is ${age} years`);
console.log(`She is a smart girl ${isSmart}`);